public class LatihanBandingNilaiProsedur {
    public static void main(String[] args) {
        int nilaiA = 10;
        int nilaiB = 20;

        if (nilaiA == nilaiB) {
            System.out.println("nilaiA sama dengan nilaiB");
        } else if (nilaiA < nilaiB) {
            System.out.println("nilaiA lebih kecil dari nilaiB");
        } else {
            System.out.println("nilaiA lebih besar dari nilaiB");
        }
    }
}